package com.shangde.edu.sp.domain;

public class SentPersonInfo {
	
	private int Id;
    private int spId;
    private int subjectId;

    
    
	public int getId() {
		return Id;
	}
	
	public void setId(int id) {
		Id = id;
	}
	
	public int getSpId() {
		return spId;
	}
	
	public void setSpId(int spId) {
		this.spId = spId;
	}
	
	public int getSubjectId() {
		return subjectId;
	}
	
	public void setSubjectId(int subjectId) {
		this.subjectId = subjectId;
	}

    
}
